import numpy as np







class op(object):
    def __init__(self):
        pass
    
class add(op):
    def __init__(self):
        pass
    
    
    @staticmethod
    def calc(a,b):
        return a+b
    
    
class mul(op):
    
    def __init__(self):
        pass
    
    @staticmethod
    def calc(a,b):
        return a*b